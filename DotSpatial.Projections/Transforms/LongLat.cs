namespace DotSpatial.Projections.Transforms
{
    /// <summary>
    /// LongLat
    /// </summary>
    public class LongLat : Transform
    {
        #region Private Variables

        private double _fromRadians;
        private double _toRadians;

        #endregion

        #region Constructors

        /// <summary>
        /// Creates a new instance of LongLat
        /// </summary>
        public LongLat()
        {
            Proj4Name = "longlat";
            Proj4Aliases = new[] { "latlon", "latlong", "lonlat" };
            Name = "WGS84";
        }

        #endregion

        #region Methods

        /// <inheritdoc />
        protected override void OnForward(double[] lp, double[] xy, int startIndex, int numPoints)
        {
            for (int i = startIndex; i < startIndex + numPoints; i++)
            {
                int phi = i * 2 + PHI;
                int lam = i * 2 + LAMBDA;
                int x = i * 2 + X;
                int y = i * 2 + Y;
                xy[x] = (lp[lam] / A) * _fromRadians;
                xy[y] = (lp[phi] / A) * _fromRadians;
            }
        }

        /// <inheritdoc />
        protected override void OnInverse(double[] xy, double[] lp, int startIndex, int numPoints)
        {
            for (int i = startIndex; i < startIndex + numPoints; i++)
            {
                int phi = i * 2 + PHI;
                int lam = i * 2 + LAMBDA;
                int x = i * 2 + X;
                int y = i * 2 + Y;
                lp[lam] = xy[x] * A * _toRadians;
                lp[phi] = xy[y] * A * _toRadians;
            }
        }

        /// <summary>
        /// Initializes the transform using the parameters from the specified coordinate system information
        /// </summary>
        /// <param name="projInfo">A ProjectionInfo class contains all the standard and custom parameters needed to initialize this transform</param>
        protected override void OnInit(ProjectionInfo projInfo)
        {
            _toRadians = projInfo.GeographicInfo.Unit.Radians;
            _fromRadians = 1 / _toRadians;
        }

        #endregion
    }
}